package byte_stream.filter_stream;

public class Student {
	public int no;
	public String name;
	public double avg;
	public char grade;
	public int tot;
	
	
	public Student(int no, String name, double avg, char grade, int tot) {
		super();
		this.no = no;
		this.name = name;
		this.avg = avg;
		this.grade = grade;
		this.tot = tot;
	}
	
	
}
